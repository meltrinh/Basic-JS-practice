// Write a condition: a > b
// Write the first action: console.log("a is bigger than b.")
// Write the second action: console.log("a is smaller than b.")
// a is assigned a value: 5
// b is assigned a value: 10

// Declare variables a and b
let a = 5;
let b = 10;

// Write an if..else statement
if (a > b) {
    console.log("a is bigger than b.");
} else if (a < b) {
    console.log("a is smaller than b.");
} else {
    console.log("a is equal to b.");
}